function changeLanguage() {
    const selectedLanguage = document.getElementById("language-select").value;
    localStorage.setItem("language", selectedLanguage);
    applyTranslations();
}

function applyTranslations() {
    const language = localStorage.getItem("language") || "pt"; // Padrão para português
    document.getElementById("welcome-message").textContent = translations[language].welcomeMessage;
    document.getElementById("search-input").placeholder = translations[language].searchPlaceholder;
    document.getElementById("search-button").textContent = translations[language].searchButton;
    document.getElementById("message").textContent = translations[language].travelMessage; // Altera aqui

    // Atualizando os itens do menu
    document.getElementById("travel-gps").textContent = translations[language].menu.travelGPS;
    document.getElementById("explore-destinations").textContent = translations[language].menu.exploreDestinations;
    document.getElementById("travel-tips").textContent = translations[language].menu.travelTips;
    document.getElementById("emergency").textContent = translations[language].menu.emergency;
    document.getElementById("support").textContent = translations[language].menu.support;
    document.getElementById("account-settings").textContent = translations[language].menu.accountSettings;
    document.getElementById("logout").textContent = translations[language].menu.logout;

    // Atualizando o título e dicas de viagem
    document.getElementById("dicas-title").textContent = translations[language].travelTips.title;
    
    for (let i = 1; i <= 8; i++) {
        document.getElementById(`tip${i}-title`).textContent = translations[language].travelTips.tips[i * 2 - 2];
        document.getElementById(`tip${i}-text`).textContent = translations[language].travelTips.tips[i * 2 - 1];
    }
}

function loadLanguage() {
    applyTranslations(); // Chama a função para aplicar traduções ao carregar a página
}

// Chame loadLanguage() no evento de carregamento da página
document.addEventListener('DOMContentLoaded', loadLanguage);
